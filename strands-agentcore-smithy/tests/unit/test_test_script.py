# ── Requirement 10.5: No nested JSON echo that breaks variable substitution ──

class TestNoNestedJsonEcho:
    def test_no_nested_echo_json_with_dollar_id_token(self):
        """Test script must not use nested echo JSON that breaks ${ID_TOKEN} substitution.

        Bad pattern: echo '{"headers": {"Authorization": "Bearer '${ID_TOKEN}'"}}' 
        This breaks because of nested quoting. The script should use a temp file
        or a heredoc approach instead.
        """
        # Look for echo commands that try to inline JSON with variable substitution
        bad_pattern = re.search(
            r"""echo\s+['"]\s*\{.*Authorization.*\$\{?ID_TOKEN""",
            TEST_TEMPLATE,
        )
        assert bad_pattern is None, (
            "Test script must not use echo with inline JSON containing ${ID_TOKEN}"
        )

    def test_uses_temp_file_or_heredoc_for_payload(self):
        """Test script should use a temp file or heredoc for the JSON payload."""
        uses_temp = "mktemp" in TEST_TEMPLATE or "PAYLOAD" in TEST_TEMPLATE
        uses_heredoc = "<<" in TEST_TEMPLATE and "EOF" in TEST_TEMPLATE
        assert uses_temp or uses_heredoc, (
            "Test script should use temp file or heredoc for JSON payload construction"
        )


